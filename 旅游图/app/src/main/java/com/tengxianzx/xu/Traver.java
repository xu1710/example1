package com.tengxianzx.xu;

public class Traver
{
	private String name;

    private int Sid;

    public Traver(String name, int Id) {
        this.name = name;
        this.Sid = Id;
    }

    public String getName() {
        return name;
    }

    public int Sid() {
        return Sid;
    }
}
